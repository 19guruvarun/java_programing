class IncrementOperations 
{
	public static void main(String[] args) 
	{
		//post increment----> " a++ "
		System.out.println("Post Increment");
		int a=0;
		System.out.println(a++);
		System.out.println(a);

		//pre increment----> " ++b "
		System.out.println("Pre Increment");
		int b=0;
		System.out.println(++b);
		System.out.println(b);


	}
}
