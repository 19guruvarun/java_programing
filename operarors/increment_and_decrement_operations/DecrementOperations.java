class DecrementOperations
{
	public static void main(String[] args) 
	{
		//post decrement----> " a-- "
		System.out.println("Post Decrement");
		int a=10;
		System.out.println(a--);
		System.out.println(a);

		//pre increment----> " ++b "
		System.out.println("Pre Decrement");
		int b=10;
		System.out.println(--b);
		System.out.println(b);

	}
}
