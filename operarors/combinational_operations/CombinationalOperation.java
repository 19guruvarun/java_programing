class CombinationalOperation
{
	public static void main(String[] args) 
	{
		int a=10;
		
		System.out.println("Value after Adding 2 to the value a of "+a+" using combinational operator += is : "+(a+=2));
		System.out.println("Value after Substracting 2 from the value a of "+a+" using combinational operator -= is : "+(a-=2));
		System.out.println("Value after Multiplicating 2 to the value a of "+a+" using combinational operator +* is : "+(a*=2));
		System.out.println("Value after dividing 2 from the value a of "+a+" using combinational operator +* is : "+(a/=2));
		System.out.println("Value after performing modulus  to the value a of "+a+" with 2 using combinational operator %= is : "+(a%=2));

	}
}
