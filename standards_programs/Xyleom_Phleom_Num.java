import java.util.Scanner;
class Xyleom_Phleom_Num 
{
	public static void main(String[] args) 
	{
		Scanner sc=new Scanner(System.in);
		System.out.println("Enter a value");
		int n=sc.nextInt();
		int sum=0;
		int ld=n%10;
		int temp=n;
		n=n/10;
		
		while(n>9){
			sum+=n%10;
			n/=10;
		}
		
		int fd=n;

		System.out.println(ld);
		System.out.println(fd);
		if(sum==(fd+ld))
			System.out.println(temp+" Xyloem number");
		else
			System.out.println(temp +" Phloem number");



	}
}
